							About EDU-SUM
							 <header id="inner">
								<h1>This is solely for the purpose of education </h1>
								 <p>In fast growing world of internet we see a lot of educational institutions and professors helping the students by uploading the video lectured online. 
On one hand these video lectures are extremely helpful for the students to learn any concept or topic or their choice of hour, these are not so effective when it comes to quick revision during exams.
Since the video lectures are more often too large or lengthy to go through students find it difficult to navigate to a particular topic they want to revise.  
So this project is an attempt to summarize the lecture videos so that the students can use it effectively for their quick revision. 
The scope of this project also entails the building a sub-topic based search. Generally in a lecture video there are many subtopics being covered. So subtopic based search helps us find only the topics of our interest.
</p>
